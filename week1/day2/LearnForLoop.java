package week1.day2;

public class LearnForLoop {

	public static void main(String[] args) {

		int num=100;
		
		//for -> press ctrl + space -> choose 2nd optiom
		for (int i = 0; i < num; i++) {
			System.out.println(i);
		}
		
		
		
		for (int i = 10; i>=0; i--) {
			System.out.println(i);
		}
		
	}

}
